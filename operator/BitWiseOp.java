package operator;

public class BitWiseOp {
    public static void main(String[] args) {
        // int n = -6;
        int a = 10;
    
        // Using left shift
        System.out.println("a<<1 : " + (a << 4));
      
        // Using right shift
        System.out.println("a>>1 : " + (a >>> 1));

        // System.out.println(n);
        // System.out.println("\n"+ ~n);



    }
}
